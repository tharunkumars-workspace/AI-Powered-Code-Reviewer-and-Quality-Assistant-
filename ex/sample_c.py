def find_max(numbers):
    """
    Find the maximum number in a list.

    Args:
        numbers (list): List of integers.

    Returns:
        int: Maximum value in list.
    """
    return max(numbers)


def is_even(n):
    """
    Check whether a number is even.

    Args:
        n (int): Input number.

    Returns:
        bool: True if even, False otherwise.
    """
    return n % 2 == 0